export function normalizeValueForProperty(
  property: Property,
  rawValue: string | boolean | string[],
): RecordValue {
  switch (property.type) {
    case "title":
    case "text":
    case "date":
      return String(rawValue);
    case "number":
      return rawValue === "" ? null : Number(rawValue);
    case "checkbox":
      return Boolean(rawValue);
    case "multiSelect":
      return Array.isArray(rawValue) ? rawValue : [];
    default:
      return Array.isArray(rawValue) ? (rawValue[0] ?? "") : String(rawValue);
  }
}

export function updateRecordValue(
  state: AppState,
  recordId: string,
  property: Property,
  rawValue: string | boolean | string[],
): AppState {
  const record = state.records[recordId];
  if (!record) {
    return state;
  }

  const nextValue = normalizeValueForProperty(property, rawValue);
  const nextRecord =
    property.type === "title"
      ? {
          ...record,
          title: nextValue == null ? "" : String(nextValue),
          updatedAt: nowIso(),
        }
      : {
          ...record,
          values: {
            ...record.values,
            [property.id]: nextValue,
          },
          updatedAt: nowIso(),
        };

  return {
    ...state,
    records: {
      ...state.records,
      [recordId]: nextRecord,
    },
  };
}
