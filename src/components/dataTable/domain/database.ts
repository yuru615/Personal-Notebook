function createDefaultOptions(): SelectOption[] {
  return DEFAULT_SELECT_OPTIONS.map((option) => ({
    id: makeId("option"),
    label: option.label,
    color: option.color,
  }));
}

function getNextOptionColor(usedColors: Set<string>, preferredIndex: number) {
  for (let offset = 0; offset < OPTION_COLOR_PALETTE.length; offset += 1) {
    const color =
      OPTION_COLOR_PALETTE[(preferredIndex + offset) % OPTION_COLOR_PALETTE.length];

    if (!usedColors.has(color)) {
      return color;
    }
  }

  return OPTION_COLOR_PALETTE[preferredIndex % OPTION_COLOR_PALETTE.length];
}

function normalizeSelectOptions(
  previousOptions: SelectOption[] | undefined,
  nextOptions: SelectOption[],
) {
  const previousById = new Map(previousOptions?.map((option) => [option.id, option]) ?? []);
  const previousByLabel = new Map(
    previousOptions?.map((option) => [option.label, option]) ?? [],
  );
  const usedColors = new Set<string>();

  return nextOptions.map((option, index) => {
    const previousOption =
      previousById.get(option.id) ?? previousByLabel.get(option.label);
    const explicitColor =
      option.color && option.color !== OPTION_COLOR_PLACEHOLDER
        ? option.color
        : null;
    const resolvedColor =
      explicitColor ??
      (previousOption?.color && previousOption.color !== OPTION_COLOR_PLACEHOLDER
        ? previousOption.color
        : !option.color || option.color === OPTION_COLOR_PLACEHOLDER
        ? getNextOptionColor(usedColors, index)
        : option.color);

    usedColors.add(resolvedColor);

    return {
      ...option,
      id: previousOption?.id ?? option.id,
      color: resolvedColor,
    };
  });
}

function areRecordValuesEqual(left: RecordValue, right: RecordValue) {
  if (Array.isArray(left) || Array.isArray(right)) {
    if (!Array.isArray(left) || !Array.isArray(right) || left.length !== right.length) {
      return false;
    }

    return left.every((value, index) => value === right[index]);
  }

  return left === right;
}

function syncOptionValue(
  propertyType: PropertyType,
  currentValue: RecordValue,
  previousOptions: SelectOption[] | undefined,
  nextOptions: SelectOption[],
): RecordValue {
  const nextOptionById = new Map(nextOptions.map((option) => [option.id, option]));
  const nextLabels = new Set(nextOptions.map((option) => option.label));
  const labelMap = new Map<string, string>();

  for (const previousOption of previousOptions ?? []) {
    const nextOption = nextOptionById.get(previousOption.id);

    if (nextOption) {
      labelMap.set(previousOption.label, nextOption.label);
    }
  }

  if (propertyType === "select") {
    if (typeof currentValue !== "string" || currentValue.length === 0) {
      return currentValue;
    }

    const mappedValue = labelMap.get(currentValue) ?? currentValue;
    return nextLabels.has(mappedValue) ? mappedValue : "";
  }

  if (propertyType === "multiSelect") {
    if (!Array.isArray(currentValue) || currentValue.length === 0) {
      return currentValue;
    }

    return currentValue.flatMap((label) => {
      const mappedValue = labelMap.get(label) ?? label;
      return nextLabels.has(mappedValue) ? [mappedValue] : [];
    });
  }

  return currentValue;
}

function getPropertyConfig(type: PropertyType): PropertyConfig {
  if (type === "select" || type === "multiSelect") {
    return { options: createDefaultOptions() };
  }

  if (type === "formula") {
    return { formulaExpression: "" };
  }

  return {};
}

function normalizePropertyConfig(
  type: PropertyType,
  config: PropertyConfig,
): PropertyConfig {
  if (type === "select" || type === "multiSelect") {
    return {
      ...config,
      options: normalizeSelectOptions(undefined, config.options ?? []),
    };
  }

  return config;
}

function patchProperty(
  property: Property,
  updates: Partial<Pick<Property, "name" | "type" | "config">>,
): Property {
  return {
    ...property,
    ...updates,
    updatedAt: nowIso(),
  };
}

function touchDatabase(
  state: AppState,
  propertyOrder = state.database.propertyOrder,
) {
  return {
    ...state.database,
    propertyOrder,
    updatedAt: nowIso(),
  };
}

function normalizePropertyKey(name: string) {
  const normalized = name
    .trim()
    .toLowerCase()
    .replace(/\s+/g, "_")
    .replace(/[^a-z0-9_]/g, "")
    .replace(/^_+|_+$/g, "");

  return normalized || "field";
}

function makeUniquePropertyKey(baseKey: string, usedKeys: Set<string>) {
  if (!usedKeys.has(baseKey)) {
    usedKeys.add(baseKey);
    return baseKey;
  }

  let suffix = 2;
  let nextKey = `${baseKey}_${suffix}`;
  while (usedKeys.has(nextKey)) {
    suffix += 1;
    nextKey = `${baseKey}_${suffix}`;
  }

  usedKeys.add(nextKey);
  return nextKey;
}

function createUniquePropertyKey(state: AppState, name: string) {
  const baseKey = normalizePropertyKey(name);
  const existingKeys = new Set(
    Object.values(state.properties).map((property) => property.key),
  );

  return makeUniquePropertyKey(baseKey, existingKeys);
}

function isTitleProperty(property: Property | undefined) {
  return property?.type === "title";
}

function createNormalizedProperty(
  property: Property,
  type: PropertyType,
  key: string,
): Property {
  const hasChanged = property.type !== type || property.key !== key;

  return {
    ...property,
    type,
    key,
    config:
      property.type === type
        ? normalizePropertyConfig(type, property.config)
        : getPropertyConfig(type),
    updatedAt: hasChanged ? nowIso() : property.updatedAt,
  };
}

type LegacyDatabaseState = {
  sort?: SortRule | null;
  filters?: FilterRule[];
  view?: Partial<DatabaseView> & {
    ganttDatePropertyId?: string | null;
  };
};

function normalizeSortRule(
  sort: SortRule | null | undefined,
  properties: Record<string, Property>,
) {
  if (!sort?.propertyId || !properties[sort.propertyId]) {
    return null;
  }

  return {
    propertyId: sort.propertyId,
    direction: sort.direction === "desc" ? "desc" : "asc",
  } satisfies SortRule;
}

function normalizeFilterRules(
  filters: FilterRule[] | undefined,
  properties: Record<string, Property>,
) {
  return (filters ?? []).filter(
    (filter) =>
      Boolean(filter?.id) &&
      Boolean(filter?.propertyId) &&
      Boolean(properties[filter.propertyId]),
  );
}

function normalizeBoardColumnOrder(
  boardGroupPropertyId: string | null,
  boardColumnOrder: string[] | undefined,
  properties: Record<string, Property>,
) {
  if (!boardGroupPropertyId) {
    return [];
  }

  const property = properties[boardGroupPropertyId];

  if (
    !property ||
    (property.type !== "select" && property.type !== "multiSelect") ||
    !property.config.options?.length
  ) {
    return [];
  }

  const optionIds = property.config.options.map((option) => option.id);
  const validOptionIds = new Set(optionIds);
  const seenIds = new Set<string>();
  const normalizedOrder = (boardColumnOrder ?? []).filter((optionId) => {
    if (!validOptionIds.has(optionId) || seenIds.has(optionId)) {
      return false;
    }

    seenIds.add(optionId);
    return true;
  });

  for (const optionId of optionIds) {
    if (!seenIds.has(optionId)) {
      normalizedOrder.push(optionId);
    }
  }

  return normalizedOrder;
}

